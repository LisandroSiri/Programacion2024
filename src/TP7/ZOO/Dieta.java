
package TP7.ZOO;


public class Dieta {
    private int codigo;
    private String tipo;

    public Dieta(int codigo, String tipo) {
        this.codigo = codigo;
        this.tipo = tipo;
    }

    public int getCodigo() {
        return codigo;
    }

    public String getTipo() {
        return tipo;
    }
    
}
